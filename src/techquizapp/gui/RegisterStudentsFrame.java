    private boolean validateInput() {
        userId=txtUserId.getText().trim();
        password=String.valueOf(txtPassword.getPassword()).trim();
        rePassword=String.valueOf(txtRePassword.getPassword()).trim();
        if(userId.isEmpty() || password.isEmpty() || rePassword.isEmpty())
            return false;
        return true;
    }
