    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        boolean check = validateInput();
        if(!check){
            JOptionPane.showMessageDialog(null,"Please fill the field","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(password.equalsIgnoreCase(rePassword)==false){
            JOptionPane.showMessageDialog(null,"Password ans RePassword must be same","Password do not match!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        User user= new User();
        user.setUserId(userId);
        user.setPassword(password);
        user.setUserType("student");
        try{
            boolean checkU=UserDAO.checkUser(userId);
            if(checkU){
            JOptionPane.showMessageDialog(null,"UserId is already available!\n Try with different userId","Error",JOptionPane.ERROR_MESSAGE);
            return;
            }
            boolean ans=UserDAO.addUser(user);
            if(!ans){
                JOptionPane.showMessageDialog(null,"Error in Adding the user","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(null,"User Added Sussesfully!","Sucess",JOptionPane.INFORMATION_MESSAGE);
            txtUserId.setText("");
            txtPassword.setText("");
            txtRePassword.setText("");

        }catch(SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null,"Error in DB","Error",JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnRegisterActionPerformed

